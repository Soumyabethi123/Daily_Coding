class Solution
{
    public:
    Node *compute(Node *head)
    {
        // your code goes here
        
        Node *curr=head;
        Node *prev=NULL;
        
        while(curr->next != NULL ){
            
            if(curr->data < curr->next->data){
                
                if(curr==head){
                    
                    head=curr->next;
                    curr=curr->next;
                    prev=NULL;
                }
                else{
                    
                    curr=curr->next;
                    prev->next=curr;
                    curr=head;
                    prev=head;
                    
                }
            }
            else{
                
                prev=curr;
                curr=curr->next;
            }
        }
        
        return head;
    }
    
};
   


